class Solution
{
public:
    bool threeConsecutiveOdds(vector<int> &arr)
    {
        int n = arr.size();

        for (int i = 0; i < n;)
        {
            int j;
            for (j = i; j < n; j++)
            {
                if (arr[j] % 2 == 0)
                    break;
            }
            if (j - i >= 2)
                return true;
            i = j;
        }
        return false;
    }
};